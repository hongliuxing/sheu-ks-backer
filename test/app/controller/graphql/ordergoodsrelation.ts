const uuidv1 = require('uuid/v1');
const util = require('util');
import * as httpUtil from '../utils/base';
import {spread} from '../utils/gql-util';

const obj = {
	id: uuidv1(), // 编号
	out_trade_no: 'out_trade_no_value', // 商户订单号
	goods_id: 'goods_id_value', // 商品编号
	goods_count: 4, // 商品数量
	created: '2016.497172696585473-2.5506703968327873-11.183984306273398 23.986735419711724:50.6673915581806:57.79761468537541', //
};

export const find = async () => {
	it('should HTTP /find', async () => {
		const q1 = `query={
			ordergoodsrelation(id: "some id"){
				id, out_trade_no, goods_id, goods_count, created
			}
		}`;

		try {
			const res: any = await httpUtil.toGet(q1);
			console.log('/graphql result ::',  util.inspect(res.body, true, 3));
		} catch (err) {
			console.log('/graphql try->catch ::', err);
		}
	});
};
